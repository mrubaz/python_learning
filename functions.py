# Write a function that checks if a number is even or odd and returns the result (not just print).
def isPrime(num):
    if num % 2 == 0:
        return print(f"{num} is a Even number")
    else:
        return print(f"{num} is an Odd number")


number = int(input("Enter a number: "))
isPrime(number)

